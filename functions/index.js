exports.createUser = functions.firestore
    .document("messages/{message}")
    .onCreate((snap, context) => {
      return admin.messaging().sendToTopic("chat", {
        notification: {
          title: "Anonymus",
          body: snap.data().text,
          clickAction: "FLUTTER_NOTIFICATION_CLICK",
        },
      });
    });
